void solve()
{
    vector<vector<int>> vec(5, vector<int>(5));
    for (int row = 0; row < 5; ++row)
    {
        for (int col = 0; col < 5; ++col)
        {
            cin >> vec[row][col];
        }
    }

    int row1, col1;

    for (int row = 0; row < 5; ++row)
    {
        for (int col = 0; col < 5; ++col)
        {
            if (vec[row][col] == 1)
            {
                row1 = row;
                col1 = col;
            }
        }
    }

    int ans = std::abs(row1 - 2) + std::abs(col1 - 2);
    cout << ans << "\n";
}


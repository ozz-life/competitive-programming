typedef long long ll;
typedef unsigned long long ull;
typedef vector<int> vi;
typedef pair<int, int> pi;

const char ENDL = '\n';

void solve()
{
    int n, m;
    cin >> n >> m;
    vector<int> vec(n);
    vector<int> ans(n + 1);
    std::set<int> st;

    for (int &x : vec)
    {
        cin >> x;
    }
    for (int i = n - 1; i >= 0; --i)
    {
        st.insert(vec[i]);
        ans[n - 1 - i + 1] = st.size();
    }

    while (m--)
    {
        int l;
        cin >> l;
        cout << ans[n - l + 1] << "\n";
    }
}


#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
typedef unsigned long long ull;
typedef vector<int> vi;
typedef pair<int, int> pi;

const char ENDL = '\n';

void solve()
{
    int64_t n, m;
    cin >> n >> m;
    std::map<std::string, std::string> mp;

    for (int64_t i = 0; i < m; ++i)
    {
        std::string a, b;
        cin >> a >> b;
        mp[a] = b;
    }

    for (int i = 0; i < n; ++i)
    {
        string str;
        cin >> str;
        string translate = mp[str];
        if (translate.size() < str.size())
        {
            cout << translate << " ";
        }
        else
        {
            cout << str << " ";
        }
    }
}


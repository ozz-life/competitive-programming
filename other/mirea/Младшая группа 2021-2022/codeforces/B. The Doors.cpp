void solve()
{
    ll n;
    cin >> n;
    vector<ll> vec(n);

    for (ll i = 0; i < n; ++i)
    {
        cin >> vec[i];
    }

    ll last_door = vec.back();
    last_door = 1 - last_door; // Инвертируем, если последняя дверь была такой-то, то теперь ищем другую
    for (ll i = n - 1; i >= 0; --i)
    {
        if (vec[i] == last_door)
        {
            cout << i + 1 << "\n";
            break;
        }
    }
}


function isLeapYear(year) {
    if (year % 4 === 0) {
        if (year % 100 === 0) {
            if (year % 400 === 0) {
                return true;
            } else {
                return false;
            }
        } else {
            return true;
        }
    } else {
        return false;
    }
}

function main() {

    let n = readLine()

    if (isLeapYear(n) == true) console.log(`YES`)
    else console.log(`NO`)
}
#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    int a, b, c;
    cin >> a;
    cin >> b;
    cin >> c;

    if (a + b <= c || a + c <= b || b + c <= a)
    {
        cout << "UNDEFINED" << endl;
    }
    else if (a * a + b * b == c * c || a * a + c * c == b * b || b * b + c * c == a * a)
    {
        cout << "YES" << endl;
    }
    else
    {
        cout << "NO" << endl;
    }

    return 0;
}


package main

import (
	"bufio"
	"fmt"
	"os"
)

/*
 * Math
 ******************************************************************************/

func gcd(a, b int) int {
	for a != 0 {
		a, b = b%a, a
	}
	return b
}
func abs(x int) int {
	if x < 0 {
		return -x
	}
	return x
}
func min(a, b int) int {
	if b < a {
		return b
	}
	return a
}
func max(a, b int) int {
	if b > a {
		return b
	}
	return a
}

/*
 * Solve
 ******************************************************************************/

 func isSquare(grid [][]byte) bool {
	n := len(grid)

	minRow, maxRow, minCol, maxCol := n, -1, n, -1
	for i := 0; i < n; i++ {
		for j := 0; j < n; j++ {
			if grid[i][j] == '1' {
				if i < minRow {
					minRow = i
				}
				if i > maxRow {
					maxRow = i
				}
				if j < minCol {
					minCol = j
				}
				if j > maxCol {
					maxCol = j
				}
			}
		}
	}

	for i := minRow; i <= maxRow; i++ {
		for j := minCol; j <= maxCol; j++ {
			if grid[i][j] != '1' {
				return false
			}
		}
	}

	return true
}

func solve(in *bufio.Reader, out *bufio.Writer) {
	var n int
	fmt.Fscan(in, &n)

	grid := make([][]byte, n)
	for j := 0; j < n; j++ {
		var line string
		fmt.Fscan(in, &line)
		grid[j] = []byte(line)
	}

	if isSquare(grid) {
		fmt.Fprintln(out, "SQUARE")
	} else {
		fmt.Fprintln(out, "TRIANGLE")
	}
}

/*
 * Main
 ******************************************************************************/

func main() {
	in := bufio.NewReader(os.Stdin)
	out := bufio.NewWriter(os.Stdout)
	defer out.Flush()

	var count_test int
	fmt.Fscan(in, &count_test)

	// var count_test int = 1;
	for i := 0; i < count_test; i++ {
		solve(in, out)
	}
}

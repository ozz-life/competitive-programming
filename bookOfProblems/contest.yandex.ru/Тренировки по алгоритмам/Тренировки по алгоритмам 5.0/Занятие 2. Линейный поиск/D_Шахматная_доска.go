/*
 * Math
 ******************************************************************************/

func gcd(a, b int) int {
	for a != 0 {
		a, b = b%a, a
	}
	return b
}
func abs(x int) int {
	if x < 0 {
		return -x
	}
	return x
}
func min(a, b int) int {
	if b < a {
		return b
	}
	return a
}
func max(a, b int) int {
	if b > a {
		return b
	}
	return a
}

/*
 * Solve
 ******************************************************************************/

func calculatePerimeter(cells [][]int) int {
	board := make([][]int, 10)
	for i := range board {
		board[i] = make([]int, 10)
	}

	for _, cell := range cells {
		x, y := cell[0], cell[1]
		board[y][x] = 1
	}

	// fmt.Println("Debug board:")
	// for y := 1; y <= 9; y++ {
	// 	for x := 1; x <= 9; x++ {
	// 		fmt.Printf("%d ", board[y][x])
	// 	}
	// 	fmt.Println()
	// }

	perimeter := 0

	for _, cell := range cells {
		x, y := cell[0], cell[1]
		perimeter += 4
		perimeter -= board[y-1][x] + board[y+1][x] + board[y][x-1] + board[y][x+1]
	}

	return perimeter
}

func solve(in *bufio.Reader, out *bufio.Writer) {
	var n int
	fmt.Fscan(in, &n)

	cells := make([][]int, n)

	for i := 0; i < n; i++ {
		var x, y int
		fmt.Fscan(in, &x, &y)
		cells[i] = []int{x, y}
	}

	perimeter := calculatePerimeter(cells)
	fmt.Fprintln(out, perimeter)
}

/*
 * Main
 ******************************************************************************/

func main() {
	in := bufio.NewReader(os.Stdin)
	out := bufio.NewWriter(os.Stdout)
	defer out.Flush()

	// var count_test int
	// fmt.Fscan(in, &count_test)

	var count_test int = 1
	for i := 0; i < count_test; i++ {
		solve(in, out)
	}
}

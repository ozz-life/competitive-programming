package main

import (
	"bufio"
	"fmt"
	"os"
	"strings"
)

/*
 * Math
 ******************************************************************************/

func gcd(a, b int) int {
	for a != 0 {
		a, b = b%a, a
	}
	return b
}
func abs(x int) int {
	if x < 0 {
		return -x
	}
	return x
}
func min(a, b int) int {
	if b < a {
		return b
	}
	return a
}
func max(a, b int) int {
	if b > a {
		return b
	}
	return a
}

/*
 * Solve
 ******************************************************************************/

//  подумай о том что если n делится на k то и 10n делится на k

func findProfitableIncome(n, k, d int) string {
	for i := 0; i < 10; i++ {
		// Подбор делимого числа
		if (n*10+i)%k == 0 {
			return fmt.Sprintf("%d%s", n*10+i, strings.Repeat("0", max(0, d-1)))
		}
	}
	return "-1"
}

func solve(in *bufio.Reader, out *bufio.Writer) {
	var n, k, d int
	fmt.Fscanf(in, "%d %d %d", &n, &k, &d)

	if n < 0 || k <= 0 || d <= 0 {
		fmt.Fprintln(out, -1)
		return
	}
	if n == 0 {
		fmt.Fprintln(out, 0)
		return
	}

	summary := findProfitableIncome(n, k, d)
	fmt.Fprintln(out, summary)
}

/*
 * Main
 ******************************************************************************/

func main() {
	in := bufio.NewReader(os.Stdin)
	out := bufio.NewWriter(os.Stdout)
	defer out.Flush()

	// var count_test int
	// fmt.Fscan(in, &count_test)

	var count_test int = 1
	for i := 0; i < count_test; i++ {
		solve(in, out)
	}
}

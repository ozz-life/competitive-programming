package main

import (
	"bufio"
	"fmt"
	"os"
)

/*
 * Math
 ******************************************************************************/

func gcd(a, b int) int {
	for a != 0 {
		a, b = b%a, a
	}
	return b
}
func abs(x int) int {
	if x < 0 {
		return -x
	}
	return x
}
func min(a, b int) int {
	if b < a {
		return b
	}
	return a
}
func max(a, b int) int {
	if b > a {
		return b
	}
	return a
}

/*
 * Solve
 ******************************************************************************/

// https://protocoder.ru/alg/datescrossing
func solve(in *bufio.Reader, out *bufio.Writer) {
	var p, v int
	var q, m int

	fmt.Fscanf(in, "%d %d\n", &p, &v)
	fmt.Fscanf(in, "%d %d", &q, &m)

	var a_start, a_end, b_start, b_end int
	a_start = p - v
	a_end = p + v
	b_start = q - m
	b_end = q + m

	var ans int
	// Непересечение
	if a_start > b_end || b_start > a_end {
		ans = (v * 2) + 1 + (m * 2) + 1
		fmt.Fprintf(out, "%d", ans)
	} else if a_start <= b_end && b_start <= b_end {
		intersection := min(a_end, b_end) - max(a_start, b_start) + 1
		ans = (v*2 + 1) + (m*2 + 1) - intersection
		fmt.Fprintf(out, "%d", ans)
	}
}

/*
 * Main
 ******************************************************************************/

func main() {
	in := bufio.NewReader(os.Stdin)
	out := bufio.NewWriter(os.Stdout)
	defer out.Flush()

	// var count_test int
	// fmt.Fscan(in, &count_test)

	var count_test int = 1
	for i := 0; i < count_test; i++ {
		solve(in, out)
	}
}

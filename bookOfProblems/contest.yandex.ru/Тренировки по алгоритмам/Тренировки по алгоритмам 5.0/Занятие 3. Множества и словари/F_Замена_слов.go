package main

import (
	"bufio"
	"fmt"
	"os"
	"strings"
)

/*
 * Math
 ******************************************************************************/

func gcd(a, b int) int {
	for a != 0 {
		a, b = b%a, a
	}
	return b
}
func abs(x int) int {
	if x < 0 {
		return -x
	}
	return x
}
func min(a, b int) int {
	if b < a {
		return b
	}
	return a
}
func max(a, b int) int {
	if b > a {
		return b
	}
	return a
}

/*
 * Solve
 ******************************************************************************/

func solve(in *bufio.Reader, out *bufio.Writer) {
	dictionary := make(map[int]map[string]struct{})
	var result []string

	inputLine, _ := in.ReadString('\n')
	inputWords := strings.Fields(inputLine)

	for _, word := range inputWords {
		length := len(word)
		if dictionary[length] == nil {
			dictionary[length] = make(map[string]struct{})
		}
		dictionary[length][word] = struct{}{}
	}

	inputLine, _ = in.ReadString('\n')
	wordsToProcess := strings.Fields(inputLine)

	for _, word := range wordsToProcess {
		found := false
		// Проверяем возможные префиксы слова в словаре
		for i := 1; i < len(word); i++ {
			prefix := word[:i]
			if set, ok := dictionary[i]; ok {
				if _, exists := set[prefix]; exists {
					result = append(result, prefix)
					found = true
					break
				}
			}
		}
		if !found {
			result = append(result, word)
		}
	}

	var output strings.Builder
	for _, s := range result {
		output.WriteString(s)
		output.WriteByte(' ')
	}

	fmt.Fprintln(out, strings.TrimSpace(output.String()))
}

/*
 * Main
 ******************************************************************************/

func main() {
	in := bufio.NewReader(os.Stdin)
	out := bufio.NewWriter(os.Stdout)
	defer out.Flush()

	// var count_test int
	// fmt.Fscan(in, &count_test)

	var count_test int = 1
	for i := 0; i < count_test; i++ {
		solve(in, out)
	}
}
